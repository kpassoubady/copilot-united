# What is data analysis?
# Data analysis is a process of inspecting, cleansing, transforming, and modeling data with the goal of discovering useful information, informing conclusions, and supporting decision-making.

# What is pandas?
# pandas is a fast, powerful, flexible and easy to use open source data analysis and data manipulation library built on top of the Python programming language.

# What is a DataFrame?
# A DataFrame is a 2-dimensional labeled data structure with columns of potentially different types. You can think of it like a spreadsheet or SQL table, or a dictionary of Series objects.

# What is a Series?
# A Series is a one-dimensional labeled array capable of holding any data type (integers, strings, floating point numbers, Python objects, etc.). The axis labels are collectively referred to as the index.
